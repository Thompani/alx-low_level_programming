#include <stdio.h>
/**
 * main - entry point
 * Description: prints if a number is zero, negative or positive
 * Return: 0
 */
int main(void)
{

char letters, letters1;

for (letters = 'a'; letters <= 'z'; letters++)
{
putchar(letters);
}
for (letters1 = 'A'; letters1 <= 'A'; letters1++)
{
putchar(letters1);
}
putchar('\n');
return (0);
}
